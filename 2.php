                <p>For most adults, there's no identifiable cause of high blood pressure. This type of high blood pressure is called primary hypertension or essential hypertension.It tends to develop gradually over many years. Plaque buildup in the arteries, called atherosclerosis, increases the risk of high blood pressure.</p>       
                <p class="list-info"><p><h3>Secondary hypertension</h3></p>
